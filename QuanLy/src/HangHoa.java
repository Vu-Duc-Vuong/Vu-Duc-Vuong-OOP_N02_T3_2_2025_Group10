public class HangHoa {
    String maHang;
    String tenHang;
    int soLuongTon;
    String nhaSanXuat;

    public HangHoa(String maHang, String tenHang, int soLuongTon, String nhaSanXuat) {
        this.maHang = maHang;
        this.tenHang = tenHang;
        this.soLuongTon = soLuongTon;
        this.nhaSanXuat = nhaSanXuat;
    }
}
